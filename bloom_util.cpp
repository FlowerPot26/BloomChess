bool checkValidXY(int x, int y) { 
    return (x >= 0 && y >= 0 && x < 8 && y < 8);
}

string i_to_string(int i) {
    vector<char> characters = {'a','b','c','d','e','f','g','h'};
    vector<char> numbers = {'8','7','6','5','4','3','2','1'};

    string return_string = "";
    return_string += characters[i%8];
    return_string += numbers[i/8];

    return return_string;

}

int string_to_i(string input_string) {
    vector<char> characters = {'a','b','c','d','e','f','g','h'};
    vector<char> numbers = {'8','7','6','5','4','3','2','1'};

    int i = 0;
    for (int y= 0; y < 8; y++) {
        if (characters[y] == input_string[0]) {
            i += y;
        }
        if (numbers[y] == input_string[1]) {
            i += 8 * y;
        }
    }

    return i;
    
}

